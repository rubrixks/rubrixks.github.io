window.addEventListener('load', function() {
    if (typeof window.ethereum !== undefined) {
        var web3 = new Web3(window.ethereum);
        const TheGame = new web3.eth.Contract(ABI, Address);

        //Detects Metamask
        let mmDetected = document.getElementById("mm-detected");
        mmDetected.innerHTML = "Metamask has been detected!";

        //Connects Metamask
        const mmEnable = document.getElementById("mm-connect");
        mmEnable.onclick = async () => {
        await ethereum.request({method: 'eth_requestAccounts'})
        mmDetected.innerHTML = "Here's the account your loss will be tied too: " + ethereum.selectedAddress;

        //Creator Begin the Game
        const beginGame = document.getElementById('BeginGameButton');
        beginGame.onclick = async () => {
           const FirstLoserMinter = 10;
           const price = await TheGame.methods.getLossBasePrice().call({from:ethereum.selectedAddress});
           var totalCost = FirstLoserMinter * price;
          await TheGame.methods.firstLoser().send({from:ethereum.selectedAddress,value:(`${totalCost}`)});
        }

        //Submit a Transaction
        var AnnounceLosses = document.getElementById('TheGameAnnounceLoss-button');
        var amount;
        AnnounceLosses.onclick = async () => {
            amount = document.getElementById('TheGameAnnounceLoss-amount').value;
            console.log(amount);
            const price = await TheGame.methods.getLossBasePrice().call({from:ethereum.selectedAddress});
            console.log(price);
            var totalLosses = await TheGame.methods.getMintedLosses().call({from:ethereum.selectedAddress});
            console.log(totalLosses);
            var totalCost = price * amount;
            console.log(totalCost);
            
          await TheGame.methods.mintLosses(ethereum.selectedAddress,amount).send({from:ethereum.selectedAddress,value:(`${totalCost}`)});
        }
        //The Game Metrics
        var LossCounter = document.getElementById('TheGameLossCount');
        LossCounter.onclick = async () => {
          var Losses = await TheGame.methods.getMintedLosses().call({from:ethereum.selectedAddress});
          LossCounter.innerHTML = "The total amount of recorded losses are: " + Losses;
        }
        var LossChecker = document.getElementById('LossChecker');
        LossChecker.onclick = async () => {
          var SelfLosses = await TheGame.methods.lossFinder(ethereum.selectedAddress).call({from:ethereum.selectedAddress});
          LossChecker.innerHTML = 'You have these many losses on record: ' + SelfLosses;
        }
        //Withdraw
        var withdraw = document.getElementById("Withdraw");
        withdraw.onclick = async () => {
          var creatorBalance = await web3.eth.getBalance(ethereum.selectedAddress);
          var contractBalance = await web3.eth.getBalance(Address);
          withdraw.innerHTML =  "Your current balance is " + creatorBalance + ". The balance of the contract is " + contractBalance;
          await TheGame.methods.withdraw().call({from:ethereum.selectedAddress});
          var newCreatorBalance = await web3.eth.getBalance(ethereum.selectedAddress);
          withdraw.innerHTML = "Your previous balance was " + creatorBalance + ". The previous contract balance was" + contractBalance + ". Your new balance is " + newCreatorBalance +".";
        }
        }
    }
    else {
        console.log("You need to download MetaMask to count your losses, announce them to the world, and pass them to your friends.");
        alert("You need to download MetaMask to count your losses, announce them to the world, and pass them to your friends.");
    }
 })

 
    


